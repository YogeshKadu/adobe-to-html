def get_path(path):
    return path.split("[")[0]

def remove_till(index):
    global html_content
    while len(stack) > index:
        last_element = stack.pop()
        html_content += f"</{formatter[get_path(last_element)]}>"

def object_to_style(element_style_object):
    style_text = ''
    key_list = element_style_object.keys()
    for key in key_list:
        if key in style_formatter:
            style_text += style_formatter[key].replace('REPLACEME', str(element_style_object[key]))
    return style_text.lower()


def format_element(element):
    global html_content
    path_list = get_path_array(element['Path'])
    for index, path in enumerate(path_list):
        if path in ignore_tags or (not element.get('Text') and not element.get('Kids')):
            continue
        element_style = {**element['Font'], **element['attributes']}
        # Remove element
        if index >= len(stack) or stack[index] != path or (index + 1 == len(path_list) and len(stack) > index):
            if stack[index] != path:
                remove_till(index)
            else:
                remove_till(index + 1)
        # Insert new element
        if len(stack) <= 0 or len(stack) <= index:
            if get_path(path) not in skip_tag_body:
                # html_content += f"<{formatter[get_path(path)]}>"
                html_content += f"<{formatter[GetPath(path)]} style=\"{object_to_style(element_style)}\">"
            stack.append(path)
        if element.get('Kids'):
            for ele in element['Kids']:
                format_element(ele)
    if get_path(path_list[-1]) not in ignore_tags and element.get('Text'):
        print(f"3. Text inserted - {element['Text']}")
        html_content += element['Text']

